// Una nota importante es que en la consola los números se muestran de diferente color a los string. Por ejemplo:

const numero1 = 20;
const numero2 = "20";

// En javasscript todos los números se crean de la misma forma, es decir, no existe int, float, etc..

const numero3 = 30;
const numero4 = 20;
const numero5 = 20.2;
const numero6 = 0.10213;
const numero7 = .10213;
const numero8 = -3;

// Otra forma de crear números, aunque menos común:

const numero9 = new Number(20);
console.log(numero9);
